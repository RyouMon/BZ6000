p1 = {"姓名": "高小一", "年龄": 18, "薪资": 30000}
p2 = {"姓名": "高小二", "年龄": 19, "薪资": 20000}
p3 = {"姓名": "高小五", "年龄": 20, "薪资": 10000}

p = [p1, p2, p3]

for each in p:
    if each["薪资"] > 15000:
        print("姓名：{}，年龄：{}， 薪资：{}。"\
              .format(each["姓名"], each["年龄"], each["薪资"]))